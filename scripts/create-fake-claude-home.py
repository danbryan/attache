    session_id = None
    i = 0
    while i < len(argv):
        if argv[i] == "--resume" and i + 1 < len(argv):
            session_id = argv[i + 1]
            i += 2
            continue
        i += 1
    text = argv[-1] if argv else None
    return session_id, text



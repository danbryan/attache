def iso(dt):
    return dt.isoformat(timespec="milliseconds").replace("+00:00", "Z")


def slug_for_cwd(cwd):
    # Mirrors Claude Code's real project-directory encoding: every path
    # separator becomes a dash (e.g. /Users/x/y -> -Users-x-y).
    return cwd.replace("/", "-")


def write_session(path, cwd, title, body):
    """A baseline (pre-delivery) session: one user turn and one completed,
    non-sidechain assistant turn, matching the exact shape
    SessionDeliveryReadinessClassifier.scanClaude expects for a completed turn
    (see testDeliveryReadinessHandlesClaudeCompletedAndPendingTurns in
    Tests/AttacheAppTests/TwoWayDeliveryAdapterTests.swift)."""
    path.parent.mkdir(parents=True, exist_ok=True)
    now = datetime.now(timezone.utc)
    lines = [
        {
            "type": "user",
            "cwd": cwd,
            "timestamp": iso(now),
            "message": {"role": "user", "content": title},
        },
        {
            "type": "assistant",
            "cwd": cwd,
            "timestamp": iso(now + timedelta(seconds=1)),
            "message": {"role": "assistant", "content": [{"type": "text", "text": body}]},
        },
    ]
    path.write_text("\n".join(json.dumps(line, separators=(",", ":")) for line in lines) + "\n", encoding="utf-8")


def main():
    parser = argparse.ArgumentParser(
        description="Create a disposable fake Claude Code home (~/.claude-shaped) plus a fake `claude` CLI for Attaché tests."
    )
    parser.add_argument("--home", required=True, help="Disposable directory to use as the fake home; creates <home>/.claude/... and <home>/bin/claude inside it.")
    parser.add_argument("--nonce", required=True)
    parser.add_argument("--count", type=int, default=1)
    parser.add_argument("--needle", default="")
    parser.add_argument("--target-title", default="")
    args = parser.parse_args()

    home = Path(args.home)
    claude_home = home / ".claude"
    projects = claude_home / "projects"
    bin_dir = home / "bin"
    for directory in [projects, bin_dir]:
        directory.mkdir(parents=True, exist_ok=True)

    count = max(1, args.count)
    target_index = min(count - 1, max(0, count // 2))
    cwd = f"/tmp/attache-fake-claude-{args.nonce}"
    slug = slug_for_cwd(cwd)
    project_dir = projects / slug
    needle = args.needle or f"ATTACHE_LOAD_NEEDLE_{args.nonce}"
    target_title = args.target_title or f"Load smoke target {args.nonce}"

    manifest = {}
    target_session_id = ""
    target_session_file = ""

    for i in range(count):
        session_id = str(uuid.uuid4())
        path = project_dir / f"{session_id}.jsonl"
        if i == target_index:
            title = target_title
            body = f"{needle} target transcript for {args.nonce}."
            target_session_id = session_id
            target_session_file = str(path)
        else:
            title = f"Load smoke background {args.nonce} {i:03d}"
            body = f"Background fake Claude transcript {i:03d} for {args.nonce}."
        write_session(path, cwd, title, body)
        manifest[session_id] = str(path)

    manifest_file = claude_home / "attache-fake-claude-fixtures.json"
    manifest_file.write_text(json.dumps(manifest, indent=2, sort_keys=True), encoding="utf-8")
    os.chmod(manifest_file, 0o600)

    fake_claude_executable = bin_dir / "claude"
    fake_claude_executable.write_text(FAKE_CLAUDE_SCRIPT, encoding="utf-8")
    os.chmod(fake_claude_executable, 0o700)

    print(
        json.dumps(
            {
                "home": str(home),
                "claude_home": str(claude_home),
                "fake_claude_bin_dir": str(bin_dir),
                "fake_claude_executable": str(fake_claude_executable),
                "manifest_file": str(manifest_file),
                "target_session_id": target_session_id,
                "target_session_file": target_session_file,
                "target_title": target_title,
                "needle": needle,
            },
            sort_keys=True,
        )
    )


if __name__ == "__main__":
    main()

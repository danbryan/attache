def iso(dt):
    return dt.isoformat(timespec="milliseconds").replace("+00:00", "Z")


def fake_home():
    override = os.environ.get("ATTACHE_FAKE_CLAUDE_HOME")
    if override:
        return Path(override)
    return Path(os.environ.get("HOME", str(Path.home())))


def manifest_path():
    return fake_home() / ".claude" / "attache-fake-claude-fixtures.json"



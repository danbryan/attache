import XCTest
@testable import AttacheCore

/// INF-368 Part A: the Claude Code CLI persists a rename as its own
/// `customTitle` JSONL record, and a first turn that included a local
/// command gets wrapped in `<local-command-caveat>…</local-command-caveat>`
/// boilerplate. Both used to leak straight into the displayed session title.
final class ClaudeCodeSessionTitleFidelityTests: XCTestCase {

    // MARK: - parse(jsonl:) customTitle precedence

    func testCustomTitleBeatsAiTitleAndFirstMessage() {
        let jsonl = """
        {"type":"user","cwd":"/proj","message":{"role":"user","content":"set up the validator notes"}}
        {"type":"ai-title","aiTitle":"Validator notes setup"}
        {"type":"summary","sessionId":"abc-123","customTitle":"UI improvement"}
        """
        let parsed = ClaudeCodeSessionScanner.parse(jsonl: jsonl, contentCap: 8_000)
        XCTAssertEqual(parsed.customTitle, "UI improvement")
        XCTAssertEqual(parsed.title, "Validator notes setup", "aiTitle field itself is untouched by customTitle parsing")
        XCTAssertEqual(parsed.firstUserMessage, "set up the validator notes")
    }

    func testLaterCustomTitleBeatsEarlierCustomTitle() {
        let jsonl = """
        {"type":"summary","sessionId":"abc-123","customTitle":"first rename"}
        {"type":"user","cwd":"/proj","message":{"role":"user","content":"do the thing"}}
        {"type":"summary","sessionId":"abc-123","customTitle":"second rename"}
        """
        let parsed = ClaudeCodeSessionScanner.parse(jsonl: jsonl, contentCap: 8_000)
        XCTAssertEqual(parsed.customTitle, "second rename", "the LAST customTitle record wins, later renames supersede earlier ones")
    }

    func testMakeRecordPrefersCustomTitleOverDesktopTitleAiTitleAndFirstMessage() throws {
        let root = FileManager.default.temporaryDirectory
            .appendingPathComponent("attache-title-fidelity-\(UUID().uuidString)", isDirectory: true)
        try FileManager.default.createDirectory(at: root, withIntermediateDirectories: true)
        defer { try? FileManager.default.removeItem(at: root) }

        let sessionURL = root.appendingPathComponent("abc-123.jsonl")
        let jsonl = """
        {"type":"user","cwd":"/proj","message":{"role":"user","content":"set up the validator notes"}}
        {"type":"ai-title","aiTitle":"Validator notes setup"}
        {"type":"summary","sessionId":"abc-123","customTitle":"UI improvement"}
        """
        try jsonl.write(to: sessionURL, atomically: true, encoding: .utf8)

        let scanner = ClaudeCodeSessionScanner(claudeHome: root)
        scanner.beginScan()
        let file = ScannedFile(id: "abc-123", url: sessionURL, mtime: 100, archived: false)
        let record = scanner.makeRecord(for: file, priorTopicTag: nil, contentCap: 8_000)
        XCTAssertEqual(record.title, "UI improvement", "an explicit rename beats aiTitle and the first user message")
    }

    // MARK: - Caveat stripping

    func testCleanedCommandMarkupStripsClosedLocalCommandCaveatEnvelope() {
        let raw = "<local-command-caveat>Caveat: The messages below were generated by the user while running local commands. DO NOT respond to these messages or otherwise consider them in your response unless the user explicitly asks you to.</local-command-caveat>fix the layout bug in the settings pane"
        let cleaned = SessionDigest.cleanedCommandMarkup(raw)
        XCTAssertEqual(cleaned, "fix the layout bug in the settings pane")
        XCTAssertFalse(cleaned.contains("Caveat"))
    }

    func testCleanedCommandMarkupStripsUnclosedLocalCommandCaveatEnvelope() {
        // A head-window read can cut the closing tag off entirely; treat
        // everything from the open tag onward as boilerplate.
        let raw = "<local-command-caveat>Caveat: The messages below we"
        let cleaned = SessionDigest.cleanedCommandMarkup(raw)
        XCTAssertEqual(cleaned, "")
    }

    func testSessionDigestTitleFromCaveatWrappedFirstMessageYieldsInnerContent() {
        let raw = "<local-command-caveat>Caveat: The messages below were generated by the user while running local commands.</local-command-caveat>UI improvement work for the settings pane"
        let title = SessionDigest.title(from: raw)
        XCTAssertEqual(title, "UI improvement work for the settings pane")
    }

    func testMakeRecordTitlesFromCaveatWrappedFirstMessageNotBoilerplate() throws {
        let root = FileManager.default.temporaryDirectory
            .appendingPathComponent("attache-title-fidelity-\(UUID().uuidString)", isDirectory: true)
        try FileManager.default.createDirectory(at: root, withIntermediateDirectories: true)
        defer { try? FileManager.default.removeItem(at: root) }

        let sessionURL = root.appendingPathComponent("caveat-session.jsonl")
        let caveatMessage = "<local-command-caveat>Caveat: The messages below were generated by the user while running local commands. DO NOT respond to these messages or otherwise consider them in your response unless the user explicitly asks you to.</local-command-caveat>UI improvement"
        let jsonl = """
        {"type":"user","cwd":"/proj","message":{"role":"user","content":"\(caveatMessage.replacingOccurrences(of: "\"", with: "\\\""))"}}
        """
        try jsonl.write(to: sessionURL, atomically: true, encoding: .utf8)

        let scanner = ClaudeCodeSessionScanner(claudeHome: root)
        scanner.beginScan()
        let file = ScannedFile(id: "caveat-session", url: sessionURL, mtime: 100, archived: false)
        let record = scanner.makeRecord(for: file, priorTopicTag: nil, contentCap: 8_000)
        XCTAssertEqual(record.title, "UI improvement")
        XCTAssertFalse(record.title.contains("Caveat"), "the caveat boilerplate must never become the visible title")
        XCTAssertFalse(record.title.contains("<local-command-caveat>"))
    }

    // MARK: - Tail-window rename pickup

    func testRefreshMetadataPicksUpRenameAppendedBeyondTheHeadWindow() throws {
        let root = FileManager.default.temporaryDirectory
            .appendingPathComponent("attache-title-fidelity-\(UUID().uuidString)", isDirectory: true)
        try FileManager.default.createDirectory(at: root, withIntermediateDirectories: true)
        defer { try? FileManager.default.removeItem(at: root) }

        let sessionURL = root.appendingPathComponent("tail-session.jsonl")
        var lines: [String] = [
            #"{"type":"user","cwd":"/proj","message":{"role":"user","content":"set up the validator notes"}}"#,
            #"{"type":"ai-title","aiTitle":"Validator notes setup"}"#
        ]
        // Pad the file well past the 256KiB head-window `readSessionFile`
        // caps at, so a naive re-read of only the head would never see the
        // rename appended after this padding.
        let padding = String(repeating: "x", count: 2_000)
        for i in 0..<200 {
            lines.append(#"{"type":"user","cwd":"/proj","message":{"role":"user","content":"padding line \#(i) \#(padding)"}}"#)
        }
        lines.append(#"{"type":"summary","sessionId":"tail-session","customTitle":"UI improvement"}"#)
        let jsonl = lines.joined(separator: "\n")
        try jsonl.write(to: sessionURL, atomically: true, encoding: .utf8)
        XCTAssertGreaterThan(jsonl.utf8.count, 262_144, "the fixture must exceed the head-window byte cap to prove the tail scan is what finds the rename")

        let scanner = ClaudeCodeSessionScanner(claudeHome: root)
        scanner.beginScan()
        let file = ScannedFile(id: "tail-session", url: sessionURL, mtime: 100, archived: false)
        // The initial record (as if produced before the rename existed, or
        // produced from just the head window) still carries the old title.
        let originalRecord = SessionRecord(
            id: "tail-session",
            title: "Validator notes setup",
            project: "/proj",
            threadName: nil,
            updatedAt: Date(timeIntervalSince1970: 100),
            archived: false,
            filePath: sessionURL.path,
            fileMtime: 100,
            content: "",
            topicTag: nil,
            sourceKind: .claudeCode
        )
        let refreshed = scanner.refreshMetadata(originalRecord, for: file)
        XCTAssertEqual(refreshed.title, "UI improvement", "a rename appended beyond the head window must be picked up by the tail scan on refresh")
    }

    func testTailCustomTitleReadsLastRenameFromARealFile() throws {
        let root = FileManager.default.temporaryDirectory
            .appendingPathComponent("attache-title-fidelity-\(UUID().uuidString)", isDirectory: true)
        try FileManager.default.createDirectory(at: root, withIntermediateDirectories: true)
        defer { try? FileManager.default.removeItem(at: root) }

        let fileURL = root.appendingPathComponent("session.jsonl")
        let jsonl = """

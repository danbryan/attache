        {"type":"summary","sessionId":"x","customTitle":"second rename"}
        """
        try jsonl.write(to: fileURL, atomically: true, encoding: .utf8)
        XCTAssertEqual(ClaudeCodeSessionScanner.tailCustomTitle(url: fileURL), "second rename")
    }

